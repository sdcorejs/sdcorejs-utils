/**
 * Component size token used across UI components to control visual density.
 *
 * - `'sm'` — small, compact layout
 * - `'md'` — medium, standard layout (default for most components)
 * - `'lg'` — large, spacious layout
 *
 * @example
 * const buttonSize: Size = 'sm';
 * // <sd-button [size]="buttonSize">Save</sd-button>
 */
export type Size = 'sm' | 'md' | 'lg';
